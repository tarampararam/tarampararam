#include "libft.h"

int		ft_atoi(const char *str)
{
	int			n;
	int			res;

	n = 1;
	res = 0;
	while (*str && (*str == ' ' || *str == '\t' || *str == '\n' ||
				*str == '\v' || *str == '\r' || *str == '\f'))
		++str;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			n = -1;
		++str;
	}
	while (*str && (*str >= '0' && *str <= '9'))
	{
		res = res * 10 + (*str - 48);
		++str;
	}
	return (res * n);
}
